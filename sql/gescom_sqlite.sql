CREATE TABLE tva (
  CODTVA INTEGER NOT NULL,
  TXTVA REAL,
  PRIMARY KEY (CODTVA)
);

CREATE TABLE branche (
  NOBRAN INTEGER NOT NULL,
  LIBBRAN TEXT,
  PRIMARY KEY (NOBRAN)
);

CREATE TABLE categorie (
  NOCAT INTEGER NOT NULL,
  LIBCAT TEXT,
  PRIMARY KEY (NOCAT)
);

CREATE TABLE famille (
  NOFAM INTEGER NOT NULL,
  LIBFAM TEXT,
  PRIMARY KEY (NOFAM)
);

CREATE TABLE secteur (
  NOSECT INTEGER NOT NULL,
  LIBSECT TEXT,
  PRIMARY KEY (NOSECT)
);

CREATE TABLE fournisseur (
  NOFOUR INTEGER NOT NULL,
  NOBRAN INTEGER,
  RSFOUR TEXT,
  RUEFRN TEXT,
  VILFRN TEXT,
  CPFRN TEXT,
  CAFRN REAL,
  PRIMARY KEY (NOFOUR)
);

CREATE TABLE article (
  NOART INTEGER NOT NULL,
  CODTVA INTEGER,
  NOFAM INTEGER,
  NOFOUR INTEGER,
  DESART TEXT,
  CAART REAL,
  QTESTO INTEGER,
  QTEMIN INTEGER,
  QTEREA INTEGER,
  REFFOU TEXT,
  STOTHE INTEGER,
  POIDS REAL,
  PRIMARY KEY (NOART)
);

CREATE TABLE representant (
  NOREP INTEGER NOT NULL,
  NOSECT INTEGER,
  NOMREP TEXT,
  PRNREP TEXT,
  RUEREP TEXT,
  VILREP TEXT,
  CPREP TEXT,
  CAREP REAL,
  DATEEMB TEXT,
  PRIMARY KEY (NOREP)
);

CREATE TABLE client (
  NOCLI INTEGER NOT NULL,
  NOCAT INTEGER,
  RSCLI TEXT,
  RUECLI TEXT,
  VILCLI TEXT,
  CPCLI TEXT,
  CACLIC REAL,
  CACLIP REAL,
  ETACLI TEXT,
  PRIMARY KEY (NOCLI)
);

CREATE TABLE commande (
  NOCDE INTEGER NOT NULL,
  NOCLI INTEGER,
  NOREP INTEGER,
  DATCDE TEXT,
  ETACDE TEXT,
  DELAIS TEXT,
  E TEXT,
  PRIMARY KEY (NOCDE)
);

CREATE TABLE lignecde (
  NOCDE INTEGER NOT NULL,
  NOART INTEGER NOT NULL,
  QTECDE INTEGER,
  ETLCDE TEXT,
  PRIMARY KEY (NOCDE,NOART)
);

CREATE TABLE livraison (
  NOLIV INTEGER NOT NULL,
  NOCDE INTEGER,
  NOCLI INTEGER,
  DATLIV TEXT,
  ETALIV TEXT,
  PRIMARY KEY (NOLIV)
);

CREATE TABLE ligneliv (
  NOLIV INTEGER NOT NULL,
  NOART INTEGER NOT NULL,
  QTELIV INTEGER,
  PRIMARY KEY (NOLIV,NOART)
);

CREATE TABLE tarif (
  NOART INTEGER NOT NULL,
  DATPRX TEXT NOT NULL,
  PRXART REAL,
  PRIMARY KEY (NOART,DATPRX)
);

CREATE TABLE facture (
  NOFACT INTEGER NOT NULL,
  NOLIV INTEGER,
  NOCLI INTEGER,
  NORGLT INTEGER,
  DATFAC TEXT,
  TOTHT REAL,
  NETCOM REAL,
  TXTVA1 REAL,
  TXTVA2 REAL,
  TXTVA3 REAL,
  TOTVA1 REAL,
  TOTVA2 REAL,
  TOTVA3 REAL,
  TOTTTC REAL,
  ETAFACT TEXT,
  DELAIS TEXT,
  REMISE REAL,
  ETFACT TEXT,
  PRIMARY KEY (NOFACT)
);

CREATE TABLE lignefact (
  NOFACT INTEGER NOT NULL,
  NOART INTEGER NOT NULL,
  QTEFACT INTEGER,
  PRXART REAL,
  TXTVA REAL,
  MTLIGHT REAL,
  MTLTVA REAL,
  PRIMARY KEY (NOFACT,NOART)
);

CREATE TABLE reglement (
  NORGLT INTEGER NOT NULL,
  NOFACT INTEGER,
  MTRGLT REAL,
  DATRGLT TEXT,
  PRIMARY KEY (NORGLT)
);

